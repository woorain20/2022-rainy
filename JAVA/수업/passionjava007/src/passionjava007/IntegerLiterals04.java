package passionjava007;

public class IntegerLiterals04 {
	public static void main(String[] args) {
		int num1=235;
		int num2=0235;
		int num3=0x235;
		
		System.out.println("num1: "+num1);
		System.out.println("num2: "+num2);
		System.out.println("num3: "+num3);
		
		System.out.println("22+36+45+60=" +(22+36+45+60));
		System.out.println("022+036+045+060=" +(022+036+045+060));
		System.out.println("0x22+0x36+0x45+0x60=" +(0x22+0x36+0x45+0x60));
	}
}
