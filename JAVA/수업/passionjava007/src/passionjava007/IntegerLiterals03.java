package passionjava007;

public class IntegerLiterals03 {
	public static void main(String[] args) {
		int num1=1234;
		int num2=01234;
		int num3=0x1234;
		
		System.out.println("num1: "+num1);
		System.out.println("num2: "+num2);
		System.out.println("num3: "+num3);
		
		System.out.println("15+25+35=" +(15+25+35));
		System.out.println("015+025+035=" +(015+025+035));
		System.out.println("0x15+0x25+0x35=" +(0x15+0x25+0x35));
	}

}
