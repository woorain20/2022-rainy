package passionjava007;

public class PrefixOp02 {
	public static void main(String[] args) {
		int num = 34;
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(--num);
		System.out.println(--num);
		System.out.println(--num);
		System.out.println(++num);
		System.out.println(num);
	}

}
