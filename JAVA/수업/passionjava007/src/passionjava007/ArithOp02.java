package passionjava007;

public class ArithOp02 {
	public static void main(String[] args) {
		
		int num1=54;
		int num2=7;
		
		System.out.println("num1+num2=" +(num1+num2));
		System.out.println("num1-num2=" +(num1-num2));
		System.out.println("num1*num2=" +(num1*num2));
		System.out.println("num1/num2=" +(num1/num2));
		System.out.println("num1%num2=" +(num1%num2));
	}

}
