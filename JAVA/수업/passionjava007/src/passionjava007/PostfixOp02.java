package passionjava007;

public class PostfixOp02 {
	public static void main(String[] args) {
		int num=452;
		System.out.println((num++)+"");
		System.out.println((num++)+"");
		System.out.println((num++)+"");
		System.out.println((num++)+"");
		System.out.println((num--)+"");
		System.out.println(num+"\n");
		System.out.println((num--)+"");
		System.out.println((num--)+"");
		System.out.println((num++)+"");
		System.out.println((num++)+"");
		System.out.println((num--)+"");
		System.out.println((num++)+"");
		System.out.println(num);
	}

}
