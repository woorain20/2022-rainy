package passionjava007;

public class ArithOp01 {
	public static void main(String[] args) {
		int num1=33;
		int num2=6;
		
		System.out.println("num1+num2=" + (num1+num2));
		System.out.println("num1-num2=" + (num1-num2));
		System.out.println("num1*num2=" + (num1*num2));
		System.out.println("num1/num2=" + (num1/num2));
		System.out.println("num1%num2=" + (num1%num2));
	}

}
