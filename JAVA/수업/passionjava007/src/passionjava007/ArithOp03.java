package passionjava007;

public class ArithOp03 {
	public static void main(String[] args) {
		int num1=45;
		int num2=4;
		System.out.println("num1+num2=" + (num1+num2));
		System.out.println("num1-num2=" + (num1-num2));
		System.out.println("num1*num2=" + (num1*num2));
		System.out.println("num1/num2=" + (num1/num2));
		System.out.println("num1%num2=" + (num1%num2));
	}

}
