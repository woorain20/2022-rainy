package passionjava007;

public class PostfixOp03 {
	public static void main(String[] args) {
		int num=35;
		System.out.println((num++)+"");
		System.out.println((num--)+"");
		System.out.println((num++)+"");
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(--num);
		System.out.println(++num);
		System.out.println(--num);
		System.out.println(++num);
		System.out.println(--num);
		System.out.println((num++)+"");
	}

}
