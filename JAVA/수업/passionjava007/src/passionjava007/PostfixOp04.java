package passionjava007;

public class PostfixOp04 {
	public static void main(String[] args) {
		int num=54;
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(--num);
		System.out.println(++num);
		System.out.println(++num);
		System.out.println(--num);
		System.out.println(--num);
		System.out.println(--num);
		System.out.println((num++)+"");
		System.out.println((num++)+"");
		System.out.println((num++)+"");
		System.out.println((num--)+"");
		System.out.println((num++)+"");
		System.out.println((num--)+"");
		System.out.println((num--)+"");
		System.out.println((num--)+"");
		System.out.println((num++)+"");
		System.out.println((num++)+"");
		System.out.println((num--)+"");
		System.out.println(--num);
		}
}
