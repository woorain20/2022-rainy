package passionjava006;

public class OperatePromotion04 {
	public static void main(String[] args) {
		short num1=23;
		short num2=11;
		short num3=4;
		short num4=14;
		int num5=(num1-num2)/num3;
		short num6=35;
		short num7=7;
		int num8=(num4+num6)/num7;
		int result=(num1+num2-num3)*num4+num5-num6+num7+num8;
		System.out.println(result);
	}

}
