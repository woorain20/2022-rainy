package passionjava006;

public class Boolean03 {
	public static void main(String[] args) {
		boolean b1=true;
		boolean b2=true;
		boolean b3=false;
		boolean b4=false;
		System.out.println(b3);
		System.out.println(b2);
		System.out.println(b4);
		System.out.println(b1);
		
		int num1=54;
		int num2=42;
		int num3=43;
		int num4=12;
		int num5=28;
		int num6=18;
		System.out.println(num1>num2+num6);
		System.out.println(num2<num4+num5+num6);
		System.out.println(num3>num2);
		System.out.println(num1<num3+num4);
		System.out.println(num1-num4<num5+num6);
		System.out.println(num3-num6<num4+num5);
	}

}
