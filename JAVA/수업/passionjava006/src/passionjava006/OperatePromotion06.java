package passionjava006;

public class OperatePromotion06 {
	public static void main(String[] args) {
		short num1=12;
		short num2=34;
		short num3=13;
		short num4=57;
		short num5=21;
		int num6=num3-num1;
		int num7=num1+num5;
		int num8=num4-num2;
		int result=num1+num2+num3;
		int result1=num4+num5-num6;
		int result2=num5*num6-num7+num8;
		int result3=num2*(num5-num3)+num8;
		int result4=num4-num1+num3+num7;
		System.out.println(result);
		System.out.println(result1);
		System.out.println(result2);
		System.out.println(result3);
		System.out.println(result4);
	}

}
